public class FogoPokemon extends Pokemon {
    private String queimaduraExplosiva;
    private String chuteLabareda;
    private String erupcao;
    private String poder;

    public FogoPokemon() {
        this.queimaduraExplosiva = queimaduraExplosiva;
        this.chuteLabareda = chuteLabareda;
        this.erupcao = erupcao;
        this.poder = poder;
    }
    



    public String getQueimaduraExplosiva() {
        return queimaduraExplosiva;
    }

    public void setQueimaduraExplosiva(String queimaduraExplosiva) {
        this.queimaduraExplosiva = queimaduraExplosiva;
    }

    public String getChuteLabareda() {
        return chuteLabareda;
    }

    public void setChuteLabareda(String chuteLabareda) {
        this.chuteLabareda = chuteLabareda;
    }

    public String getErupcao() {
        return erupcao;
    }

    public void setErupcao(String erupcao) {
        this.erupcao = erupcao;
    }
    
    public String getPoder() {
        return poder;
    }

    public void setPoder(String poder) {
        this.poder = poder;
    }
}
