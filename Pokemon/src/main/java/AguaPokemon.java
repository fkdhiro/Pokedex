public class AguaPokemon extends Pokemon {
    private String aquaJato;
    private String bolha;
    private String mergulho;
    private String poder;

    public AguaPokemon() {
        this.aquaJato = aquaJato;
        this.bolha = bolha;
        this.mergulho = mergulho;
        this.poder = poder;
    }


    public String getAquaJato() {
        return aquaJato;
    }

    public void setAquaJato(String aquaJato) {
        this.aquaJato = aquaJato;
    }

    public String getBolha() {
        return bolha;
    }

    public void setBolha(String bolha) {
        this.bolha = bolha;
    }

    public String getMergulho() {
        return mergulho;
    }

    public void setMergulho(String mergulho) {
        this.mergulho = mergulho;
    }

    public String getPoder() {
        return poder;
    }

    public void setPoder(String poder) {
        this.poder = poder;
    }
}
